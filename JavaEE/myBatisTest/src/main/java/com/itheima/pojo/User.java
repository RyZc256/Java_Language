package com.itheima.pojo;

public class User {
    private int uid;
    private String uname;
    private int uage;

    public void setUid(int uid) {
        this.uid = uid;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public void setUage(int uage) {
        this.uage = uage;
    }

    public int getUid() {
        return uid;
    }
    public String getUname() {
        return uname;
    }

    public int getUage() {
        return uage;
    }
}